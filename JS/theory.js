// it is interpreted language.
// issue with js is it is single threaded language.
// it can do one task at a time.
// to solve this issue we have async programming.
// async programming is a way of programming that allows multiple tasks to be performed concurrently without blocking the main thread.
// it is achieved using callbacks, promises and async/await.

// ---------------------------------------------------------------------------------------------------------------------
//  ************** let const and var in js:*********************
// var is function scoped.
// let and const are block scoped.
// var can be re-declared and updated.
// let can be updated but not re-declared.
// const cannot be updated or re-declared.
// var is hoisted to the top of its scope and initialized with undefined.
// let and const are hoisted to the top of their scope but not initialized.
// this is called temporal dead zone.
// to avoid these issues it is recommended to use let and const instead of var.
